num=[10,20,30,40]
print(num)
print(num[0])
print(num[1])
print(num[2])
#To add single value
num.append(50)
print(num)
#To add multiple values
num.extend([60,70,80])
print(num)
#To insert at specific index
num.insert(2,75)
print(num)
#To remove a value
num.remove(30)
print(num)
#To pop a value at specific index
num.pop(5)
print(num)
#To reverse the list
num.reverse()
print(num)
#To Sort
num.sort()
print(num)
#To clear the list
num.clear()
print(num)


